/*
@功能：utf8编码转gb2312
@说明：utf8占用3字节存储1个汉字，gb2312占用2字节存储1个汉字，HZK库是gb2312编码故需做兼容转换
@参数utf8：1个utf-8编码的汉字
@参数gb2312：1个gb2312编码的汉字
*/

int utf8_to_gb2312(unsigned char utf8[3],unsigned char gb2312[2]){
    
	unsigned char *pi1	= utf8;
    unsigned char **pi2 = &pi1;
    unsigned char *po1  = gb2312;
    unsigned char **po2 = &po1;
    size_t        ilen  = 3;
    size_t        olen  = 2;
	iconv_t cd;

    if((cd = iconv_open("gbk","utf-8")) == (iconv_t)-1){
      //printf("iconv open fail \n");
        return 1;
    }else
    {
      //printf("iconv open ok \n");    
    }
    
    iconv(cd,(char**)pi2,&ilen,(char**)po2,&olen);
	iconv_close(cd);

}

/*
@功能：取汉字字模
@参数utf8：1个utf-8编码的汉字
*/

void get_model(unsigned char utf8[3]){

	FILE *fp;
	unsigned long offset;
	//unsigned char model[32];
	unsigned char gb2312[3] = {0};
	utf8_to_gb2312(utf8,gb2312);
	offset=((gb2312[0]-0xa1)*94+(gb2312[1]-0xa1))*32;
	//printf("%x",offset);
	if ((fp = fopen("HZK16","r"))==NULL){
		//printf("read fail \n");
	}
    fseek(fp,offset,SEEK_SET);
	fread(model,32,1,fp);
	fclose(fp);	

}

/*
@功能：在指定位置显示1个汉字
@说明：屏幕左上角竖向下横向右建立xy坐标系
@参数x：1~4
@参数y：1~8
@参数chs：utf-8编码的1个汉字
*/

void draw_zh(int x,int y,unsigned char chs[3]){

	unsigned char a[16],b[16];
	get_model(chs);
	memcpy(a,model,16);
	memcpy(b,model+16,16);
	memcpy(frame+((x-1)*256)+((y-1)*16),a,16);
	memcpy(frame+(128+(x-1)*256)+((y-1)*16),b,16);

}

/*
@功能：在指定位置开始显示最多不超过8个汉字
@说明：屏幕左上角竖向下横向右建立xy坐标系
@参数x：1~4
@参数y：1~8
@参数chs：最多1~8个utf-8编码的汉字
*/

void draw_zh_line(int x,int y,unsigned char w[24]){
	
	int i;
	unsigned char tmp[3];
	//printf("%d\n",strlen(w));
	for(i=1;i<=(strlen(w)/3);i++){
		memcpy(tmp,w+(i-1)*3,3);
		draw_zh(x,y++,tmp);
	}

}

